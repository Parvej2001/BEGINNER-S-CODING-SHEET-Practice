//5) Write a Program to Find Size of int, float, double and char in your computer.
#include<iostream>
using namespace std;
int main(){
    cout<<"Size of integer is : "<<sizeof(int)<<endl;
    cout<<"Size of float is : "<<sizeof(float)<<endl;
    cout<<"Size of double is : "<<sizeof(double)<<endl;
    cout<<"Size of char is : "<<sizeof(char);
}
//OUTPUT
// Size of integer is : 4
// Size of float is : 4
// Size of double is : 8
// Size of char is : 1